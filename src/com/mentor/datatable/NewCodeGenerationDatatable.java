package com.mentor.datatable;

import java.util.Date;

public class NewCodeGenerationDatatable {

	private int uploadedId;
	private int barCodeCount;
	private String type;
	private Date date;
	private String status;
	private int distillery_id;
	private String finalizeFlag;
	private String datelink;
	
	
	
	
	
	
	
	
	public String getDatelink() {
		return datelink;
	}
	public void setDatelink(String datelink) {
		this.datelink = datelink;
	}
	public String getFinalizeFlag() {
		return finalizeFlag;
	}
	public void setFinalizeFlag(String finalizeFlag) {
		this.finalizeFlag = finalizeFlag;
	}
	public int getDistillery_id() {
		return distillery_id;
	}
	public void setDistillery_id(int distillery_id) {
		this.distillery_id = distillery_id;
	}
	public int getUploadedId() {
		return uploadedId;
	}
	public void setUploadedId(int uploadedId) {
		this.uploadedId = uploadedId;
	}
	public int getBarCodeCount() {
		return barCodeCount;
	}
	public void setBarCodeCount(int barCodeCount) {
		this.barCodeCount = barCodeCount;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
